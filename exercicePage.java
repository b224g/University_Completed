    public void Table(){
        String []Student={"id","variante","exo"};
        String []Montrer= new String[3];
        
        model=new DefaultTableModel(null,Student);
        
        String sql="select * from exercice";
        
        try{
            Connect ();
            Statement st=conn.createStatement();
            rs=st.executeQuery(sql);
            while(rs.next()){
                Montrer[0]=rs.getString("id");
                Montrer[1]=rs.getString("variante");
                Montrer[2]=rs.getString("exo");
                
                model.addRow(Montrer);
              
            }
            jTable1.setModel(model);
            conn.close();
        }catch (Exception e){
            e.printStackTrace();
        }
    }
   

  
