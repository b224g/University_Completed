    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int id=jTable1.getSelectedRow();
        String select = (String)jTable1.getValueAt(id, 0);
        
        try{
            Connect();
            pst=conn.prepareStatement("Update exercice set variante=?,exo=?"   
                    + "where id=?");                                        
            pst.setString(1, variantetxt.getText());
            pst.setString(2, exotxt.getText());
            pst.setString(3, select);
            pst.executeUpdate();
            conn.close();
            JOptionPane.showMessageDialog(null, "Exercice Modifier");
            exercicePage.super.dispose();
            new exercicePage().setVisible(true);
            Table();
        }catch (Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

